''' my_mult.py
Create a my_mult function which takes 2 parameters (nbr1 and nbr2) and returns a value.
This value is the result of the multiplication of nbr1 and nbr2 parameters.
'''

def my_mult(num1, num2):
    return num1 * num2

print(my_mult(0, 1))
print(my_mult(10, 10))
print(my_mult(10, -10))